package Autoboxing;

public class Autoboxing {

	public static void main(String[] args) {
		// data types can convert into wrapper class
		int i = 100;
		double d = 33.7;
		float f = 33.3f;
		char c = 'k';
	    long l = 9293662129l;
	    
	    
	    // assigined wrapper classes 
	    Integer I = i;
	    Double D = d;
	    Float F = f;
	    Character C = c;
	    Long L = l;
	    
	    System.out.println(I+100);
	    System.out.println(D+10);
	    System.out.println(F+20);
	    System.out.println(C+"rishna");
	    System.out.println(L+10);
	}

}
